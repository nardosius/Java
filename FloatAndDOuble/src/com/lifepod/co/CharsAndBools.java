package com.lifepod.co;

public class CharsAndBools {
    public char myChar = 'D';
    public char myUnicodeChar = '\u0044';
    public char copyright = '\u00A9';


    public char getMyChar() {
        return myChar;
    }

    public char getMyUnicodeChar() {
        return myUnicodeChar;
    }

    public char getCopyright() {
        return copyright;
    }

    public boolean isTrue() {
        return true;
    }

    public boolean isFalse() {
        return false;
    }
}
